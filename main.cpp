// header file library that lets us work with input and output objects
#include <iostream>

// entry point for C and C++ programs
int main() {

  // prints a string of characters into the screen, in this case "Hello world!", std::cout indicates that cout is in the standard (std) namespace, endl indicates the line ends
  std::cout << "Hello world!" << endl;
  
  // terminates a program successfully
  return 0;
}
